HandTest = TestCase("HandTest");

HandTest.prototype.testIsEqualTo = function() {
    var h = new Hand(new Point(2, 3), new GateBlock([Gate.X, Gate.CONTROL]), 0);
    assertTrue(h.isEqualTo(h));
    assertFalse(h.isEqualTo(null));
    assertFalse(h.isEqualTo(""));

    assertTrue(h.isEqualTo(new Hand(new Point(2, 3), new GateBlock([Gate.X, Gate.CONTROL]), 0)));
    assertFalse(h.isEqualTo(new Hand(new Point(3, 3), new GateBlock([Gate.X, Gate.CONTROL]), 0)));
    assertFalse(h.isEqualTo(new Hand(new Point(2, 3), new GateBlock([Gate.X, Gate.ANTI_CONTROL]), 0)));
    assertFalse(h.isEqualTo(new Hand(new Point(2, 3), new GateBlock([Gate.X, Gate.CONTROL]), 1)));
};
